#escape sequence
name="sahana is good girl\nthe best student" #\n is used for new line
print(name)

a="sahana is\tbest" #\t is used for tab space
print(a)

a="sahana \"book\"" #\" is used to print double quotes
print(a)

#backslash
a="sahana is a good girl\\student" #\\ is used to print backslash
print(a)

a="i m she\\her"
print(a)

